var Edad = prompt("Introduce la edad");



if(Edad < 5){
    document.write("jardin de infancia")
}else if (Edad > 5 && Edad <11){
    document.write("Primaria")
}else if (Edad > 11 && Edad <16){
    document.write("ESO")
}
else if (Edad > 16 && Edad < 21){
    document.write("Bachi")
}else if (Edad > 21 ){
    document.write("UNI")
}